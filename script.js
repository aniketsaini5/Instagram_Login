function updatePageContent() {
    const selectedLanguage = document.getElementById('language-selector').value;
    const text = languageText[selectedLanguage];

    document.title = text.header;
    document.getElementById('username').placeholder = text.usernamePlaceholder;
    document.getElementById('password').placeholder = text.passwordPlaceholder;
    document.querySelector('button[type="submit"]').textContent = text.loginButton;
    document.querySelector('.or-text').textContent = text.orText;
    document.querySelector('.facebook-login a').textContent = text.facebookLogin;

    // Corrected selectors for the "Forgot Password?" link and footer links
    document.getElementById('fgt').textContent = text.forgotPassword;

    // Update the "Don't have an account? Sign Up" link text
    document.getElementById('no').textContent = text.signUp;
    document.querySelector('.card.mt-2 .card-body.text-center span').textContent = text.dont;


    // Update footer links
    const footerLinks = document.querySelectorAll('.text-center a.text-secondary');
    for (let i = 0; i < footerLinks.length; i++) {
        footerLinks[i].textContent = text.footerLinks[i];
    }
}

const languageSelector = document.getElementById('language-selector');
languageSelector.addEventListener('change', updatePageContent);

updatePageContent();


